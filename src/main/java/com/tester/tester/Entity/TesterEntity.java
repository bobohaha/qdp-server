package com.tester.tester.Entity;

public class TesterEntity {

    int id;
    int business;
    int week;
    String requireName;
    int allBugNum;
    int majorBugNum;
    int generaBugNum;
    String remarks;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getBusiness() {
        return business;
    }

    public void setBusiness(int business) {
        this.business = business;
    }

    public int getWeek() {
        return week;
    }

    public void setWeek(int week) {
        this.week = week;
    }

    public String getRequireName() {
        return requireName;
    }

    public void setRequireName(String requireName) {
        this.requireName = requireName;
    }

    public int getAllBugNum() {
        return allBugNum;
    }

    public void setAllBugNum(int allBugNum) {
        this.allBugNum = allBugNum;
    }

    public int getMajorBugNum() {
        return majorBugNum;
    }

    public void setMajorBugNum(int majorBugNum) {
        this.majorBugNum = majorBugNum;
    }

    public int getGeneraBugNum() {
        return generaBugNum;
    }

    public void setGeneraBugNum(int generaBugNum) {
        this.generaBugNum = generaBugNum;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }
}
